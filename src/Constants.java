public class Constants {

    public static final int T = 4;//定义一个常量类

    public static void main(String[] args){
        final int S = 3;//这是个常量定义
        double v = 5.6;
        int t = 8;
        System.out.println("one\n"+S*v);
        System.out.println("two\n"+S*t);
        System.out.println("san\n"+T*t);
    }
}
